# List methods used in list.

l1 = [100, 2, 5, 15, 98, 34]
l1.sort()  #This will sort the list
l1.reverse() #This will reverse the order of a list
l1.append(8)  #This will add the element at the end
l1.insert(2, 200) #This will insert the element at desired position/index
l1.pop(2)  #Remove element at index 2
l1.remove(5) #Removes the element given in the bracket
print(l1)